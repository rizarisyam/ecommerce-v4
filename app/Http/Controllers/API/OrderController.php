     */
    public function update(Request $request, $id)
    {
        $order = Order::findOrFail($id);
        $date = Carbon::now();
        $order->update([
            'expedition_price' => $request->input('expeditionPrice'),
            'status' => $request->input('status'),
            'confirm_at' => $date->toDateTimeString(),
            'confirm_by' => $request->input('user')
        ]);

        return response()->json('sukses');
    }

